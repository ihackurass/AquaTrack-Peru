    private void btnlogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlogActionPerformed
        String username = txtusernamelog.getText();
        String password = new String(txtpasswordlog.getPassword());
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Faltan Campos A Completar", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (username.length() <= 3 || password.length() <= 3) {
            JOptionPane.showMessageDialog(this, "El Usuario y Password deben tener m치s de 5 caracteres", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            login(username, password);
        }

    }//GEN-LAST:event_btnlogActionPerformed

