    private void login(String username, String password) {
        try {
            JSONObject loginResult = UserClient.login(username, password);
            boolean success = loginResult.getBoolean("success");
            String message = loginResult.getString("message");
            if (success) {
                JOptionPane.showMessageDialog(this, message, "Exitoso", JOptionPane.INFORMATION_MESSAGE);
                if (jCheckBox1.isSelected()) {
                    prefs.put("username", username);
                    prefs.put("password", password);
                } else {
                    prefs.remove("username");
                    prefs.remove("password");
                }
                boolean security = UserClient.isLoggedIn();
                if (security) {
                    PantallaPrincipal dashboard = new PantallaPrincipal();
                    dashboard.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Token Invalido", "Error", JOptionPane.ERROR_MESSAGE);

                }

            } else {
                JOptionPane.showMessageDialog(this, message, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

